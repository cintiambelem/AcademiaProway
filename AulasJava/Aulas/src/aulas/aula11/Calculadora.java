package modulo1.aula11;

public class Calculadora {

    int somar(int n1, int n2) {
        int resultado = n1 + n2;
        return resultado;
    }

    int subtrair(int n1, int n2) {
        int resultado = n1 - n2;
        return resultado;
    }

    int dividir(int n1, int n2) {
        int resultado = n1 / n2;
        return resultado;
    }

    int multip(int n1, int n2) {
        int resultado = n1 * n2;
        return resultado;
    }

}
