function somar(n1, n2) {
    let soma = n1 + n2;
    return soma;
}

function subtrair(n1, n2) {
    let subtrair = n1 - n2;
    return subtrair;
}

function dividir(n1, n2) {
    let dividir = n1 / n2;
    return dividir;
}

function multiplicar(n1, n2) {
    let multiplicar = n1 * n2;
    return multiplicar;
}

const r1 = somar(5,2)
console.log(r1);

const r2 = subtrair(5,2)
console.log(r2);

const r3 = multiplicar(5,2)
console.log(r3);

